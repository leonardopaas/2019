<section id="rece">
  <h1 class="text3">
  	<br>
  	Ingredientes :
  	<br>
  	<br>
  	• 8 claras
  	<br>
  	• 1 e ½ xícara (chá) de açúcar
  	<br>
  	• 8 gemas
  	<br>
  	• 2 xícaras (chá) + 4 colheres (sopa) de chocolate em pó (240 g)
  	<br>
    • 240 g de manteiga derretida
    <br>
    2 colheres (sopa) de conhaque
    <br>
  	<br>
  	Modo de preparo :
  	<br>
  	<br>
  	Na batedeira, bata as claras em neve. Aos poucos, acrescente o açúcar e as gemas e bata por 2 minutos. Em velocidade baixa, adicione, aos poucos, o chocolate e, em seguida, a manteiga derretida. Bata por mais 1 minuto.
    <br>
    Retire 1 xícara (chá) da massa de chocolate e misture com o conhaque. Reserve.
    <br>
    Transfira o restante da massa para uma fôrma redonda de fundo removível (25 cm de diâmetro) forrada com papel-manteiga untado e polvilhado com chocolate e leve ao forno médio preaquecido (180°C) por 20 minutos (o centro ainda estará mole). Deixe esfriar.
    <br>
    Espalhe a massa com conhaque sobre o bolo já frio e desenforme. Sirva gelado.
  </h1>