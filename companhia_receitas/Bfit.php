<section id="rece">
  <h1 class="text3">
  	<br>
  	Ingredientes :
  	<br>
  	<br>
  	• ¼ de xícara de óleo vegetal;
  	<br>
  	• 1 ½ xícara de biomassa de banana verde
  	<br>
  	• 4 ovos caipiras ou orgânicos ou 4 col sopa cheias de “ovo de linhaça”
  	<br>
  	• 4 colheres (sopa) de cacau em pó
  	<br>
    • 1 xícara de açúcar mascavo
    <br>
    • 200g de chocolate 70% derretido
    <br>
    • 1 xícara de nozes ou castanhas picadas
    <br>
  	<br>
  	Modo de preparo :
  	<br>
  	<br>
  	Bata todos os ingredientes no liquidificador, exceto o chocolate e as nozes, até obter uma massa homogênea. Adicione o chocolate derretido e as nozes e misture. Despeje na forma forrada com papel alumínio (mais fácil para desenformar, pode usar também forma de silicone), untado com óleo e asse no forno pré-aquecido 180°C por aproximadamente 15 a 20 minutos. O brownie estará pronto quando sentir cheirinho de bolo de chocolate no ambiente! As bordas estarão assadas e o centro ainda cremoso. Desenforme depois de frio. O brownie com ovos de linhaça leva o dobro de tempo para assar.
    <br>
    Ovo de linhaça: 1 colher de sopa linhaça ou chia (triturados) e 2 colheres (sopa) de água e deixar hidratar por 15 minutos.
  </h1>